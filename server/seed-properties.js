async function seedProperties() {
  try {
    console.log("Starting to seed properties...");

    // Clear existing properties
    await prisma.propertyImage.deleteMany();
    await prisma.propertyVideo.deleteMany();
    await prisma.property.deleteMany();

    console.log("Cleared existing properties");

    // Add sample properties
    for (const property of sampleProperties) {
      const propertyWithSlug = {
        ...property,
        slug: createSlug(property.title),
      };

      const createdProperty = await prisma.property.create({
        data: propertyWithSlug,
      });
      console.log(`Created property: ${createdProperty.title}`);
    }

    console.log("✅ Successfully seeded all properties!");
  } catch (error) {
    console.error("❌ Error seeding properties:", error);
  } finally {
    await prisma.$disconnect();
  }
}

seedProperties();
